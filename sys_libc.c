#include <sys_libc.h>


void uitox(unsigned int a, char *b)
{
  int i, i1;
  char c;
  
  if (a==0) { b[0]='0'; b[1]=0; return ;}
  
  i=0;
  while (a>0)
  {
    int num = a%16;

    if (num < 10)
      b[i]=num+'0';
    else
      b[i]=num-10+'A';

    a=a/16;
    i++;
  }
  
  for (i1=0; i1<i/2; i1++)
  {
    c=b[i1];
    b[i1]=b[i-i1-1];
    b[i-i1-1]=c;
  }
  b[i]=0;
}

